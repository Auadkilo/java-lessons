// Тема урока: Ссылочный тип данных String

package Lesson3;

// String - строковый тип данных. Относится к ссылочным.

public class Lesson3 {
    public static void main(String[] args) {
        // String - класс, поэтому с большой буквы
        String name = "Andrey";
        String surname = "Zakharov";

        // Конкатенация - создание новой строки
        System.out.println(name + " " + surname);
    }
}
