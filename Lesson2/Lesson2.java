// Тема урока: Примитивные типы данных

package Lesson2;

public class Lesson2 {
    public static void main(String[] args) {

        // INT - 4 байта
        int myInt = 1000000;

        // SHORT - 2 байта
        short myShort = 1000;

        // LONG - 8 байт
        long myLong = 1000000000;

        // DOUBLE - 8 байт
        double myDouble = 100.00;

        // FLOAT - 4 байта (обязательно "f" в конце)
        float myFloat = 100.00f;

        // CHAR - 2 байта
        char myChar = 'c';

        // BOOLEAN - 1 байт
        boolean myBollean = true;

        // BYTE - 1 байт
        byte myByte = 127;
    }
}
